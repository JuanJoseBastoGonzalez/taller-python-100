letra = input("Ingrese una letra: ")
letra = letra.upper()  
if letra == "V":
    print("Visual Basic")
elif letra == "P":
    print("Pascal")
elif letra == "C":
    print("C#")
elif letra == "J":
    print("Java")
elif letra == "F":
    print("Fortran")
else:
    print("Letra no reconocida")
